package Graph;

public class Graph {

    private float meanEdge;
    private int edgeNumber;
    private int vertexNumber;

    public Graph() {

    }

    public float getMeanEdge() {
        return meanEdge;
    }

    public int getEdgeNumber() {
        return edgeNumber;
    }

    public int getVertexNumber() {
        return vertexNumber;
    }
}
